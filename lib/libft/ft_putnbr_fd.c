#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	s;

	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
	}
	if (n < 0 && n != -2147483648)
	{
		n = n * -1;
		write(fd, "-", 1);
	}
	if (n >= 0 && n <= 9)
	{
		s = n + '0';
		write(fd, &s, 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		s = n % 10 + '0';
		write(fd, &s, 1);
	}
}
